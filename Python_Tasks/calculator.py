def add (num1, num2) :
    
    '''
        Parameters:
            num1(int) : Any integer
            num2(int) : Any integer
            
        Returns:
            Returns the addition of num1 and num2
    '''
    
    return num1 + num2


def sub(num1, num2):
    
     
    '''
        Parameters:
            num1(int) : Any integer
            num2(int) : Any integer
            
        Returns:
            Returns the subtraction of num1 and num2
    '''
    
    return num1 - num2


def mul(num1, num2) :
    
     
    '''
        Parameters:
            num1(int) : Any integer
            num2(int) : Any integer
            
        Returns:
            Returns the multiplication of num1 and num2
    '''
    
    return num1 * num2


def div(num1, num2):
    
     
    '''
        Parameters:
            num1(int) : Any integer
            num2(int) : Any integer
            
        Returns:
            Returns the division of num1 and num2
    '''
    
    return num1 / num2
